<h3>10 askelta runsauteen -nettikurssi 12.10.2015-30.5.2016</h3>
<p></p>
<h1>Millaisen taloudellisen ihmeen sinä tarvitset?</h1>
	<ul>
    <li>Täytyyk&ouml; sinun saada yritys py&ouml;rimään ansaitaksesi toimeentulon itsellesi ja perheellesi?</li>
    <li>Oletko kurkkuasi my&ouml;ten täynnä velkoja ja haluat saada ne maksetuiksi niin pian kuin mahdollista?</li>
    <li>Onko sinun aika päästä unelmiesi lomamatkalle?</li>
    <li>Haluatko oman kodin, johon voit asettua perheesi kanssa niin pitkäksi aikaa kuin haluat, paikan jossa olet täysin turvassa ja onnellinen?</li>
    <li>Haluatko lopultakin nauttia siitä, että rahaa jää muuhunkin kuin perustarpeisiin?</li>
</ul> <br>
<img src="kuvat/10-ask-runsauteen.jpg" align="right">
<p>Sinulla on suuri visio taloudellisesta runsaudesta, johon sisältyy kaikki mitä voit koskaan haluta. 
Suuri visiosi sisältää niin paljon runsautta, että sinun ei tarvitse koskaan huolehtia rahasta eikä 
miettiä aina joka senttiä. Rahaa on enemmän kuin kylliksi ja voit käyttää sitä omien ja perheesi 
unelmien toteuttamiseen, hyväntekeväisyyteen tai ihan mihin tahansa haluat.<br><br>
Suuri taloudellinen vision on jotain, jonka luomiseen menee useimmiten vuosia silloinkin, 
kun ihmisellä ei ole sisäisiä esteitä niin suuren runsauden luomiseen. Jos sinulla kuitenkin
 - niin kuin useimmilla meistä - on paljon sisäisiä esteitä runsauden luomiselle ja 
 vastaanottamiselle, suuren vision toteuttamiseen menee vielä kauemmin, jos ylipäätään 
 saat itsestäsi irti lähteä toteuttamaan sitä.<br><br>
10 askelta runsauteen -nettikurssilla alat kulkea kohti suurinta mahdollista taloudellista 
visiotasi - vaikka et vielä edes tietäisi mikä se on. Sydämesi ja sielusi tietävät, ja 
nettikurssilla luot yhteyden sisäiseen viisauteesi ja hyvyyteesi niin, että alkaessasi 
luoda runsautta et tee sitä pelosta ja ahneudesta vaan sydämestä ja sielusta käsin.<br><br>
Sydämen ja sielun visio runsaudestasi on kuitenkin niin iso, että sitä on vaikea käsittää, 
ellei ala pilkkoa sitä osiin. Siksi kysyin sinulta, millaisen taloudellisen ihmeen tarvitset, 
ja tarkoitan, millaisen ihmeen tarvitset juuri nyt.<br><br>
Koska suuren vision toteuttaminen vie vuosia ja pystyäksesi uskomaan vision toteutumiseen 
sinun on nähtävä käytänn&ouml;ssä, että osia visiostasi toteutuu. Näitä vision osia kutsun 
välietapeiksi. Taloudellinen ihme, jota tarvitset juuri nyt, on ensimmäinen välietappisi.
Osallistuessasi 10 askelta runsauteen -nettikurssille sinulla ei tarvitse olla aavistustakaan 
siitä, miten pääset välietappiisi. Sinä saat olla peloissasi ja epäuskoinen. 
Nettikurssilla etenemme askel askeleelta kohti välietappiasi ja samalla kohti 
lähemmäs suuren taloudellisen visiosi toteutumista.<br><br>
<b>Mitä jos taloudellisen runsauden luominen voi olla kivaa, kevyttä ja taianomaista?</b><br><br>
Oikeasti taloudellisen runsauden luominen on helppoa. Vaikeaksi sen tekee ainoastaan sisäiset esteesi, rajoittavat uskomukset, jotka sanovat sinulle, että taloudellisen runsauden luominen ei ole mahdollista, tai että se on äärimmäisen vaikeaa.
<b>Rajoittavat uskomukset saattavat ilmetä esimerkiksi näin:</b></p>
<ul>
    <li>Et usko pystyväsi saamaan (parempaa) ty&ouml;paikkaa, joten et edes hae, tai ty&ouml;n hakeminen tuntuu sinusta ty&ouml;läältä ja uuvuttavalta</li>
    <li>Et usko yrittäjänä saavasi tarpeeksi asiakkaita tai et tunne voivasi pyytää riittävää hintaa palveluistasi</li>
    <li>Elämä on vuosi toisensa jälkeen jatkuvaa, uuvuttavaa taloudellista kamppailua, mutta et tee mitään muuttaaksesi tilannettasi</li>
    <li>Yrityksesi muuttaa asioita tuntuvat raskailta ja uuvuttavilta ja johtavat umpikujiin ja epäonnistumisiin</li>
    <li>Haluaisit pitää välivuoden tai ryhtyä taiteilijaksi tai perustaa yrityksen, mutta et uskalla, koska et luota siihen, että olet taloudellisesti turvattu muutoksen jälkeenkin</li>
    <li>Teet harkitsemattomia rahaan liittyviä päät&ouml;ksiä joita kadut jälkeenpäin, ja jotka huonontavat tilannettasi entisestään</li>
</ul> 
<p>Rajoittavat uskomukset estävät sinua toteuttamasta unelmiasi. Unelmasi ovat sydämesi ja 
sielusi ääni, joka kuiskii sinulle elämästä, jota oikeasti haluat elää. Unelmien toteutuminen 
toisi sinulle syvempää onnea ja täyttymystä, kuin olet koskaan kokenut. Niin kauan kuin et 
anna itsellesi lupaa toteuttaa unelmiasi, et sen sijaan voi koskaan olla syvästi onnellinen, 
koska torjuessasi unelmasi torjut aidoimman itsesi.<br><br>
Sinulla on oikeus elää elämää, jota sydämesi ja sielusi haluavat sinulle. 
Elämää, jossa olet aidosti oma itsesi ja koet syvää onnea, täyttymystä, 
turvaa, keveyttä, vapautta, iloa ja luottamusta.<br><br>
10 askelta runsauteen -nettikurssi antaa sinulle välineet luoda taloudellista 
runsautta pystyäksesi kokemaan kaiken, mitä sydämesi ja sielusi haluavat sinun 
kokevan. Raha on sidoksissa moneen unelmaan. 10 askelta runsauteen -nettikurssilla 
opit tuntemaan rahan todellisen luonteen ja astumaan sisäiseen voimaasi, etkä koe 
tätä sidosta enää negatiivisena. Opit iloitsemaan rahasta, sen vastaanottamisesta 
ja käyttämisestä, ja koska olet rahan kanssa tekemisissä joka päivä, tämä muutos 
tuo elämääsi valtavasti lisää iloa, varsinkin jos raha on tähän asti synnyttänyt 
sinussa pelkoa, huolta, ahdistusta, syyllisyyttä tai häpeää. Ilo tuo mukanaan 
keveyttä, keveys helppoutta, ja näin positiivinen kierre on valmis.<br><br>
<b>10 askelta runsauteen -nettikurssi sisältää:</b><br><br></p>
<ul>
    <li>Kuukausittainen 1,5 t videomentorointi netissä</li>
    <li>Joka kuukausi 3 lyhyempää viikkomentorointia, jotka auttavat sinua pysymään suunnassa ja vievät syvemmälle kuukausimentorointien aiheisiin</li>
    <li>Äänitteet kaikista videomentoroinneista</li>
    <li>Joka kuukausi voimakas manifestointi, joka auttaa sinua luomaan todeksi haaveilemasi runsauden</li>
    <li>Arkiston kaikista mentoroinneista ja manifestoinneista</li>
    <li>Facebook-ryhmän, jossa voit turvallisesti jakaa ajatuksiasi ja kokemuksiasi toisten kurssilaisten kanssa</li>
    <li>2 henkil&ouml;kohtaista mentorointia</li>
    <li>Ylimääräistä rahaa -manifestointiäänite, joka auttaa sinua</li>
    <li>4 kk:n tuki nettikurssin loputtua</li>
</ul> 

<p><b>1) Kuukausimentoroinnit</b><br>
10 askelta runsauteen -nettikurssi kestää 8 kuukautta, ja joka kuukausi pidän 1,5 tunnin pituisen videomentoroinnin, jonka aikana käydään läpi yksi 10 taloudelliseen runsauteen johtavasta askeleesta. Osa askeleista kulkee mukana koko nettikurssin ajan ja syvenee kuukausi kuukaudelta. Kuukausimentoroinnit pidetään suorina videolähetyksinä netissä, mutta jos et pysty osallistumaan livenä tai haluat palata mentorointeihin my&ouml;hemmin, voit katsoa ne koska tahansa tallenteina. Kuukausimentoroinnit pidetään joitakin poikkeuksia lukuunottamatta kuukauden ensimmäisenä maanantaina klo 18-19.30.
<br><br>
<b>2) Viikkomentoroinnit</b><br>
Joka kuukausi saat lisäksi kolme lyhyempää viikkomentorointia, jotka auttavat sinua pysymään oikeassa suunnassa. Viikkomentoroinnit syventävät kuukausimentoroinneissa käsiteltyjä asioita ja muistuttavat sinua runsaudesta, jota alat kokea ja nähdä ympärilläsi. Viikkomentoroinnit ovat n. 5-15 min. pituisia. Niitä ei pidetä suorina lähetyksinä. Viikkomentoroinnit julkaistaan maanantaisin kuukausimentorointien välissä.
<br><br>
<b>3) Äänitteet mentoroinneista</b><br>
Saat jokaisesta kuukausi- ja viikkomentoroinnista my&ouml;s ladattavan mp3-äänitteen, jonka voit tallentaa mobiililaitteeseesi ja kuunnella koska ja missä tahansa.
<br><br>
<b>4) Manifestoinnit</b><br>
Jokaisen kuukausimentoroinnin lopussa teemme n. 15 minuutin pituisen intuitiivisesti ohjatun manifestoinnin. Manifestoinnit auttavat sinua suuntaamaan ajatuksesi, mielikuvasi ja tunteesi tavalla, joka laittaa asioita tapahtumaan näkymätt&ouml;mällä tasolla. Kun olet ladannut tarpeeksi tunnetta ja energiaa näkymätt&ouml;mälle tasolle, asioita alkaa tapahtua my&ouml;s konkreettisesti.
<br><br>
<b>5) Arkisto</b><br>
Kurssin arkistosta eli aarreaitasta l&ouml;ytyvät kaikki siihen asti julkaistut mentoroinnit ja manifestoinnit.
<br><br>
<b>6) Facebook-ryhmä</b><br>
Kurssin omassa Facebook-ryhmässä voit jakaa prosessiasi toisten kurssilaisten kanssa turvallisessa ilmapiirissä.
<br><br>
<b>7) 2 henkil&ouml;kohtaista mentorointia</b><br>
Tunnin pituiset henkil&ouml;kohtaiset mentoroinnit tapahtuvat puhelimitse. Ensimmäinen mentoroinneista sijoittuu kurssin alkuun ja toinen loppuun. Henkil&ouml;kohtaisten mentorointien aikana sinulla on tilaisuus ty&ouml;skennellä juuri itsellesi ajankohtaisten tai haastavien asioiden parissa rahaan ja taloudelliseen tilanteeseen liittyen. Molempiin mentorointeihin sisältyy my&ouml;s henkil&ouml;kohtainen, intuitiivisesti ohjattu manifestointi, joka on suunnattu juuri sinulle. Nämä henkil&ouml;kohtaiset manifestoinnit ovat usein vielä voimakkaampia kuin ryhmille suunnatut manifestoinnit. Saat molemmista mentoroinneista äänitteet, joiden kautta voit kuunnella erityisesti manifestointeja yhä uudelleen ja uudelleen, aina kun tarvitset vahvistusta.
<br><br>
<b>8) Ylimääräistä rahaa -manifestointi</b><br>
15 min. pituinen ladattava mp3-äänite, joka auttaa sinua vetämään puoleesi ja huomaamaan tarjolla olevaa rahaa, mahdollisuuksia joita et juuri nyt näe, mutta jotka voivat parantaa taloudellista tilannettasi nopeastikin, jos osaat tarttua niihin.
<br><br>
<b>9) 4 kuukauden tuki nettikurssin loputtua</b><br>
Sisältää 4 n. 15 minuutin pituista videomentorointia kerran kuussa 10 askelta runsauteen -nettikurssin loputtua. Näin saat tukea 8 kk:n sijasta 12 kk:n ajan. Videomentoroinnit syventävät tietämystäsi runsauden ja rahan manifestoinnista ja auttavat sinua jatkamaan matkaasi kohti suurta taloudellista visiotasi.
<br><br>
<b>10 askelta runsauteen -nettikurssi alkaa 12.10.2015 ja loppuu 30.5.2016.</b>
<br><br>
<b>Oletko valmis sitoutumaan taloudellisen visiosi toteutumiseen?</b><br><br>
Ilmoittaudu 10 askelta runsauteen -nettikurssille ainoastaan, jos olet valmis ja 
halukas sitoutumaan suuren taloudellisen visiosi ja ensimmäisen välietappisi toteutumiseen.
Ennen kuin sitoudut, sinun on syytä tietää, mitä suuren taloudellisen visiosi ja 
ensimmäisen taloudellisen ihmeesi toteutuminen vaativat sinulta.<br><br>
<b>Aika</b><br>
10 askelta runsauteen -nettikurssi kuljettaa sinua askel askeleelta kohti 
ensimmäistä taloudellista ihmettäsi ja vie sinua lähemmäs suurta visiotasi. 
Askelten ottaminen vaatii, että katsot tai kuuntelet joka kuukausi 1,5 tunnin 
mentoroinnin, joka tukee, opastaa ja inspiroi sinua luodessasi taloudellista 
ihmettäsi todeksi. Kuukausimentorointien tueksi saat 3 kertaa kuussa 5-15 min. 
pituisen viikkomentoroinnin, joka auttaa ajatuksiasi ja tunteitasi pysymään oikeassa suunnassa.
Kaikkein tärkein taloudellisen ihmeen manifestoinnin vaatima ajallinen panostus on 
kuitenkin (lähes) päivittäinen manifestointi. Voit toteuttaa tämän itsellesi 
parhaalta tuntuvalla tavalla, mutta nettikurssilla saat joka kuukausi 
voimakkaan, n. 15 min. pituisen manifestointiäänitteen, joita sinun 
kannattaa luonnollisesti hy&ouml;dyntää niin paljon kuin mahdollista. 
Manifestointiäänitteet tekevät manifestoinnin todella helpoksi ja kivaksi. 
Sinun ei tarvitse ponnistella suunnataksesi ajatuksesi ja tunteesi oikein, 
vaan riittää että laitat manifestointiäänitteen soimaan ja annat minun tehdä 
ty&ouml;n puolestasi. Manifestointiäänitteissä ohjaan ajatuksiasi, tunteitasi ja 
energiaasi oikeaan suuntaan niin, että alat uskoa ihmeesi toteutumiseen ja innostua siitä.<br>
<br><b>Raha</b><br>
Voit luoda taloudellista runsautta ilman, että sinun täytyy osallistua yhdellekään 
maksulliselle kurssille tai ostaa yhtään tuotetta. Tietoa ja tukea on saatavan 
ilmaiseksi. Maksullisten tuotteiden, kuten 10 askelta runsauteen -nettikurssin, 
eteen joku on kuitenkin nähnyt paljon vaivaa. 10 askelta runsauteen -nettikurssin 
tekijänä olen käyttänyt ja käytän koko kurssin ajan paljon aikaa, energiaa sydäntäni 
luodakseni kurssin, joka tekee taloudellisen runsauden luomisen sinulle 
mahdollisimman helpoksi, nopeaksi ja taianomaiseksi.<br><br>
Runsauden luominen vaatii luottamusta, sitä että uskaltaa laittaa rahaa 
virtaamaan oikealla tavalla, oikeissa olosuhteissa. Runsautta ei voi luoda, 
jos ei uskalla päästää irti rahasta, vaan tarrautuu kynsin hampain kiinni 
siihen, mitä on. Käyttäessäsi rahaa viisaasti ja sydämellä raha virtaa 
takaisin luoksesi. Sinä tiedät syvällä sisälläsi, onko 10 askelta runsauteen 
-nettikurssi sinua varten. Luota sisäiseen viisauteesi ja tee päät&ouml;ksesi sitä kunnioittaen.<br><br>
<b>Rajoittavista uskomuksista vapautuminen</b><br>
Toisinaan rajoittavat uskomukset ovat niin syviä, että niiden muuttaminen muuttaa ihmisen persoonaa, sitä mihin hän on samaistanut ja identifioinut itsensä. Oletko sinä valmis luopumaan syyllisyydestä, jota taloudellisen runsauden haluaminen sinussa synnyttää? Oletko valmis luopumaan turvasta, jota asioiden pitäminen samalla lailla kuin aina ennenkin, näennäisesti sinulle tuo? Oletko oikeasti valmis kokemaan itsesi sellaisena, kuin sydämessäsi ja sielussasi olet - vapaana, elinvoimaisena ilon, rakkauden ja runsauden lapsena, joka nauttii elämästä syvemmin, rohkeammin ja näkyvämmin kuin koskaan ennen?
Tämä muutos voi olla kipeä. Joudut luopumaan osasta identiteettiäsi ja astumaan uuteen, tuntemattomaan. Tämä tuntuu ajoittain epämukavalta, vaikealta ja pelottavalta, vaikka onkin moninkertaisesti kaiken epämukavuuden arvoista. Tämä tie ei ole kaikkia varten. Se vaatii valtavasti rohkeutta ja halua kasvaa. Oma sydämesi ja sielusi kertovat, onko tämä tie, jota sinun tulee kulkea?
